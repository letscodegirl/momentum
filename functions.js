const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },

  sub: function (a, b) {
    console.log(a - b);
  },

  div: function (a, b) {
    console.log(a / b);
  },

  multi: function (a, b) {
    console.log(a * b);
  },

  power: function (a, b) {
    console.log(a ** b);
  }

};

calculator.add(5, 1);
calculator.sub(5, 1);
calculator.div(5, 1);
calculator.multi(5, 1);
calculator.power(5, 1);